public class RowsColums {

	public static void main(String[] args) {
		

		for (int r=0; r<10; r++)  { //row
			
			for (int c=0; c<5; c++) {//colums
				System.out.print("#");
			}
			
			System.out.println();
	
		}
	
	
		System.out.println("--------------------");
	
		for (int a=1; a<=5; a++) {
			
			for (int b=1; b<5; b++){
				System.out.print(a);
			}
				System.out.println();
		}

			
		
		
		
		
		
		
		
		
		
		System.out.println("------------------");
		
			for (int i=1; i<=5; i++) {
				
				for (int j=1; j<5; j++){
					System.out.print(j);
				}
					System.out.println();
			}
	
			System.out.println("-----------------");
			for (int n=1; n<5; n++) {
				
				for (int m=1; m<10; m++){
					System.out.print(m);
				}
					System.out.println();
			}
	
			System.out.println("-----------------------");
	
				for (int x=5; x>0; x--) {
				
				for (int y=1; y<=5; y++){
					System.out.print(x);
				}
					System.out.println();
			}
	
	
	
	
	
	
	
	}

}
