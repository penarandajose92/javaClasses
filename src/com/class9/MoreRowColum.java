package com.class9;

public class MoreRowColum {

	public static void main(String[] args) {
		
			for (int i=1; i<=5; i++) {
				for (int j=1; j<=i; j++) {
					System.out.print("*");
				}
			
					System.out.println();
			}
	
			System.out.println("--------------------");
	
			for (int i=1; i<=5; i++) {
				for (int j=1; j<=i; j++) {
					System.out.print(j);
				}
			
					System.out.println();
			}
	
			System.out.println("--------------------");
				
			
			for (int x=1; x<=9; x++) {
				for(int y=1; y<=x; y++) {
					System.out.print(x);
				}
					System.out.println();
			}
			
			
			
			
	
	}

}
